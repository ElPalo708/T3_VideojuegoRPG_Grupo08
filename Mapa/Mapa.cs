    public void Dibujar(Jugador jugador)
    {
        Console.Clear();

        for (int y = 0; y < mapa.GetLength(0); y++)
        {
            for (int x = 0; x < mapa.GetLength(1); x++)
            {
                if (x == jugador.PosX && y == jugador.PosY)
                {
                    Console.Write('P');
                }
                else
                {
                    Console.Write(mapa[y, x]);
                }
            }
            Console.WriteLine();
        }
    }

    public void MoverJugador(Jugador jugador, int nuevoX, int nuevoY)
    {
        if (mapa[nuevoY, nuevoX] != '#')
        {
            jugador.PosX = nuevoX;
            jugador.PosY = nuevoY;
